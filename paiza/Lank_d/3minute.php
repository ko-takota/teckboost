あなたは気象庁のデータ分析の担当者で、8 月の台風の上陸の間隔についてのデータを作るよう求められました。
サンプルとなる5 個の台風について上陸した日が与えられるので、それぞれ (2 個目以降) について直前の上陸日との間隔日数を出力してください。


<?php
    $d_1 = intval(fgets(STDIN));
    $d_2 = intval(fgets(STDIN));
    $d_3 = intval(fgets(STDIN));
    $d_4 = intval(fgets(STDIN));
    $d_5 = intval(fgets(STDIN));
    
    $array = [$d_1, $d_2, $d_3, $d_4, $d_5];
    for ($i = 0; $i < count($array); $i++) {
         $interval = $array[$i] - $array[$i-1];
         echo ($interval."\n");
    }
?>
