D180:音のうなり
オーケストラなどで合奏をする際、演奏前に全ての楽器が同じ高さの音を出してチューニングを行います。
このとき、他の楽器と自分の楽器の高さが少しでも異なると、音がうなって聞こえてしまいます。
他の楽器の音の高さを F_1 とし、自分の楽器の音の高さを F_2 としたとき、うなりの程度は |F_1 - F_2| で求められます。
他の楽器の音の高さと自分の楽器の音の高さが与えられるので、うなりの程度がどの程度あるか求めるプログラムを作成してください。

<?php
    $input_line = explode(" ",fgets(STDIN));
    $F_1 = $input_line[0];
    $F_2 = $input_line[1];
    
    echo abs($F_1 - $F_2);
?>

